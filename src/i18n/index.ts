for (const language of APP_LANGUAGES) {
  const replayCatalog = replayMessages[language];
  Object.assign(messages[language], replayCatalog && Object.keys(replayCatalog).length > 0 ? replayCatalog : replayMessages.en);
  const holdemCatalog = holdemMessages[language];
  Object.assign(messages[language], holdemCatalog && Object.keys(holdemCatalog).length > 0 ? holdemCatalog : holdemMessages.en);
  if (messageOverrides[language]) {
    Object.assign(messages[language], messageOverrides[language]);
  }
  if (replayOverrides[language]) {
    Object.assign(messages[language], replayOverrides[language]);
  }
  if (holdemOverrides[language]) {
    Object.assign(messages[language], holdemOverrides[language]);
  }
}

export function t(language: AppLanguage, key: string, vars?: Record<string, string | number>): string {
  const fallbackTemplate =
    language === 'zh-CN'
      ? messages['zh-CN'][key]
      : messages.en[key] ?? messages['zh-CN'][key];
  const template = messages[language][key] ?? fallbackTemplate ?? key;
  if (!vars) {
    return template;
  }
  return template.replace(/\{(\w+)\}/g, (_, token) => String(vars[token] ?? ''));
}

export function getLanguageLabel(language: AppLanguage): string {
  return LANGUAGE_OPTIONS.find((option) => option.key === language)?.label ?? LANGUAGE_OPTIONS[0].label;
}
