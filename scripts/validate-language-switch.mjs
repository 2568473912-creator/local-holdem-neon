import { chromium } from 'playwright';
import fs from 'node:fs/promises';
import path from 'node:path';

const outDir = '/Users/klaywei/Documents/local-holdem-neon/output/i18n-pass';
await fs.mkdir(outDir, { recursive: true });
const browser = await chromium.launch({ headless: true });
const page = await browser.newPage({ viewport: { width: 1440, height: 1024 } });
const errors = [];
page.on('pageerror', (error) => errors.push(String(error)));
page.on('console', (msg) => { if (msg.type() === 'error') errors.push(msg.text()); });
await page.goto('http://127.0.0.1:4186', { waitUntil: 'networkidle' });
await page.screenshot({ path: path.join(outDir, 'hub-zh.png') });
await page.selectOption('.legal-entry-dock select', 'en');
await page.waitForFunction(() => document.querySelector('.game-hub-card h1')?.textContent?.includes('Neon Card Club'));
const hubTitleEn = await page.locator('.game-hub-card h1').innerText();
const shopBtnEn = await page.locator('.game-hub-actions .btn').first().innerText();
await page.screenshot({ path: path.join(outDir, 'hub-en.png') });
await page.click('.game-hub-mode-card-holdem .btn');
await page.waitForFunction(() => document.querySelector('.menu-card h1')?.textContent?.includes('Hold’em'));
const holdemTitleEn = await page.locator('.menu-card h1').first().innerText();
await page.screenshot({ path: path.join(outDir, 'holdem-en.png') });
await page.click('.legal-entry-button');
await page.waitForFunction(() => document.querySelector('.menu-shop-head strong')?.textContent?.includes('Tournament Shop'));
const shopTitleEn = await page.locator('.menu-shop-head strong').first().innerText();
await page.screenshot({ path: path.join(outDir, 'shop-en.png') });
await page.keyboard.press('Escape');
await page.waitForTimeout(150);
await page.click('.submenu-head .btn');
await page.waitForFunction(() => document.querySelector('.game-hub-card'));
await page.selectOption('.legal-entry-dock select', 'ja');
await page.click('.game-hub-mode-card-doudizhu .btn');
await page.waitForFunction(() => document.querySelector('.ddz-menu-card h1')?.textContent?.includes('ネオン斗地主'));
const ddzTitleJa = await page.locator('.ddz-menu-card h1').innerText();
await page.screenshot({ path: path.join(outDir, 'doudizhu-ja.png') });
await page.click('.ddz-menu-switch .btn');
await page.waitForFunction(() => document.querySelector('.game-hub-card'));
await page.selectOption('.legal-entry-dock select', 'de');
await page.click('.game-hub-mode-card-guandan .btn');
await page.waitForFunction(() => document.querySelector('.gd-menu-card h1')?.textContent?.includes('Guandan'));
const guandanTitleDe = await page.locator('.gd-menu-card h1').innerText();
await page.screenshot({ path: path.join(outDir, 'guandan-de.png') });
const stateText = await page.evaluate(() => window.render_game_to_text?.() ?? '{}');
await fs.writeFile(path.join(outDir, 'state.json'), stateText);
await fs.writeFile(path.join(outDir, 'summary.json'), JSON.stringify({ hubTitleEn, shopBtnEn, holdemTitleEn, shopTitleEn, ddzTitleJa, guandanTitleDe, errors }, null, 2));
await browser.close();
