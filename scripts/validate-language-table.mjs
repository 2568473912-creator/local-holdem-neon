import { chromium } from 'playwright';
import fs from 'node:fs/promises';
import path from 'node:path';
const outDir = '/Users/klaywei/Documents/local-holdem-neon/output/i18n-table-pass';
await fs.mkdir(outDir, { recursive: true });
const browser = await chromium.launch({ headless: true });
const page = await browser.newPage({ viewport: { width: 1440, height: 1024 } });
const errors = [];
page.on('pageerror', (e) => errors.push(String(e)));
page.on('console', (msg) => { if (msg.type() === 'error') errors.push(msg.text()); });
await page.goto('http://127.0.0.1:4186', { waitUntil: 'networkidle' });
await page.selectOption('.legal-entry-dock select', 'en');
await page.click('.game-hub-mode-card-holdem .btn');
await page.waitForFunction(() => document.querySelector('.menu-card h1')?.textContent?.includes('Hold’em'));
await page.click('.menu-card > .btn.primary.big');
await page.waitForFunction(() => document.querySelector('.top-hud'));
await page.screenshot({ path: path.join(outDir, 'table-en.png') });
const topTitle = await page.locator('.top-hud h1').innerText();
const nextHandLabel = await page.locator('.hud-next-hand').innerText();
const detailsLabel = await page.locator('.hud-more-info').innerText();
const actionPanelButton = await page.locator('.bottom-dock .btn, .focus-dock .btn').first().innerText().catch(() => '');
const state = await page.evaluate(() => window.render_game_to_text?.() ?? '{}');
await fs.writeFile(path.join(outDir, 'state.json'), state);
await fs.writeFile(path.join(outDir, 'summary.json'), JSON.stringify({ topTitle, nextHandLabel, detailsLabel, actionPanelButton, errors }, null, 2));
await browser.close();
